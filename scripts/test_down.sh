#!/usr/bin/env bash
set -euo pipefail

# ============================================================================
# test_down.sh - Остановка тестовых контейнеров
# ============================================================================
#
# Останавливает только тестовый проект (wednesday_test), не затрагивая
# другие локальные контейнеры. Используется в конце CI pipeline после
# всех тестов.
#
# Требования:
# - Остановка только тестового проекта (изоляция)
# - Удаление volumes (-v) для полной очистки
# - Опциональная очистка файлов покрытия (закомментирована для отладки)
#
# ============================================================================

COMPOSE_CMD="${COMPOSE_CMD:-}"
if [ -z "${COMPOSE_CMD}" ]; then
COMPOSE_PROJECT="wednesday_test"
  COMPOSE_FILE="tests/docker-compose.test.yml"
  COMPOSE_ENV="tests/.env.test"
  COMPOSE_CMD="docker compose -p ${COMPOSE_PROJECT} -f ${COMPOSE_FILE} --env-file ${COMPOSE_ENV}"
fi

echo "=== Остановка тестовых контейнеров ==="

# Остановка контейнеров с удалением volumes
${COMPOSE_CMD} down -v || true

echo "✓ Тестовые контейнеры остановлены"

