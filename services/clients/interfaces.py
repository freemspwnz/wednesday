"""
Интерфейсы клиентов внешних ML‑сервисов.

Здесь определены структурные Protocol‑интерфейсы для:

- текст‑к‑изображению (`ITextToImageClient`);
- текст‑к‑тексту (`ITextToTextClient`).

Интерфейсы намеренно не содержат деталей HTTP‑реализации
(`aiohttp`, `requests`, заголовки, схемы URL и т.п.), чтобы:

- не привязывать бизнес‑логику к конкретным транспортам;
- позволить mypy валидировать любые замены/моки по структуре;
- упростить тестирование и подмену реализаций в рантайме.
"""

from __future__ import annotations

from typing import Protocol, runtime_checkable


@runtime_checkable
class ITextToImageClient(Protocol):
    """Интерфейс клиента текст‑к‑изображению.

    Реализация отвечает за обращение к внешнему API и возврат байтов изображения.
    Бизнес‑логика (кеш, метрики, circuit breaker) реализуется поверх этого интерфейса.

    Интерфейс включает методы для управления моделями и проверки статуса API,
    что позволяет унифицировать работу с различными TTI‑провайдерами через контейнер.
    """

    async def generate(self, prompt: str, user_id: str | None = None) -> bytes | None:
        """Генерирует изображение по текстовому промпту.

        Args:
            prompt: Текстовое описание изображения.
            user_id: Идентификатор пользователя (для трейсинга и логирования), опционально.

        Returns:
            Байтовое представление изображения или None при ошибке.
        """

    async def check_api_status(
        self, save_models: bool = True
    ) -> tuple[bool, str, list[str], tuple[str | None, str | None]]:
        """Проверяет статус API и валидность ключа без генерации изображения (dry-run).

        Args:
            save_models: Флаг, указывающий, нужно ли сохранять список доступных моделей.

        Returns:
            Кортеж (успех_проверки, сообщение_о_статусе, список_моделей, (текущий_id, текущее_имя)).
            Для провайдеров, которые не поддерживают расширенный статус, можно вернуть
            упрощённые значения (например, пустой список моделей и (None, None) для текущей модели).
        """

    async def get_available_models(self, save_models: bool = True) -> list[str]:
        """Возвращает список доступных моделей.

        Args:
            save_models: Флаг, указывающий, нужно ли сохранять список доступных моделей.

        Returns:
            Список доступных моделей. Для провайдеров, которые не поддерживают
            получение списка моделей, можно вернуть пустой список.
        """

    async def set_model(self, model_identifier: str) -> tuple[bool, str]:
        """Устанавливает текущую модель для генерации изображений.

        Args:
            model_identifier: ID модели или название (или часть названия).

        Returns:
            Кортеж: (успех, сообщение). Для провайдеров, которые не поддерживают
            смену модели, можно вернуть (False, "Смена модели не поддерживается").
        """


@runtime_checkable
class ITextToTextClient(Protocol):
    """Интерфейс клиента текст‑к‑тексту (LLM).

    Реализация отвечает за вызов модели, подготовку/нормализацию ответа
    и возврат итоговой строки без бизнес‑обвязки бота.
    """

    async def generate(self, prompt: str, user_id: str | None = None) -> str | None:
        """Генерирует текстовый ответ по текстовому промпту.

        Args:
            prompt: Текстовый запрос/инструкция для модели.
            user_id: Идентификатор пользователя (для трейсинга и логирования), опционально.

        Returns:
            Сгенерированный текст или None при ошибке.
        """

    async def check_api_status(self) -> tuple[bool, str]:
        """Проверяет статус API и валидность ключа без траты токенов (dry-run).

        Returns:
            Кортеж: (успех_проверки, сообщение_о_статусе)
        """

    async def get_available_models(self, save_models: bool = True) -> list[str]:
        """Возвращает список доступных моделей.

        Args:
            save_models: Флаг, указывающий, нужно ли сохранять список доступных моделей.

        Returns:
            Список доступных моделей.
        """

    async def set_model(self, model_name: str) -> tuple[bool, str]:
        """Устанавливает текущую модель для генерации текста.

        Args:
            model_name: Название модели.

        Returns:
            Кортеж: (успех, сообщение)
        """
